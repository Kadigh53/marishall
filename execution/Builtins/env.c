#include "../../Includes/header.h"

void	env(void **env)
{
	int	i;
	int fd;

	i = 0;
	while (env[i])
	{
		write(fd,env[i],ft_strlen(env[i]));
		write(1,"\n",1);
		i++;
	}
}
